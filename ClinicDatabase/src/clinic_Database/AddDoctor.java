    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

       
        
        String[] FullName = jTextField1.getText().split(" ");
        String fname, lname, mname;
        if(FullName.length == 2){
            fname = FullName[0];
            lname = FullName[1];
            mname = "";
        }
        else if(FullName.length == 1){
            fname = FullName[0];
            lname = "";
            mname = "";
        }
        else{
            fname = FullName[0];
            lname = FullName[2];
            mname = FullName[1];
        }
        String Gender = jComboBox1.getSelectedItem().toString();
        String Phone = jTextField6.getText().trim();
        String[] DoB = jTextField2.getText().split("/");
        int date,month,year;
        date =  Integer.parseInt(DoB[0]);
        month = Integer.parseInt(DoB[1]);
        year = Integer.parseInt(DoB[2]);
        String licenseNo = jTextField8.getText().trim();
        String docPassword = jTextField17.getText().trim();
        String[] joinDate = jTextField12.getText().split("/");
        int jdate,jmonth,jyear;
        jdate =  Integer.parseInt(DoB[0]);
        jmonth = Integer.parseInt(DoB[1]);
        jyear = Integer.parseInt(DoB[2]);
        
        String houseNo = jTextField9.getText().trim();
        String street = jTextField11.getText().trim();
        String landmark = jTextField13.getText().trim();
        String city = jTextField15.getText().trim();
        String state = jTextField14.getText().trim();
        String pincode=jTextField16.getText().trim();
        
        try{
            boolean doctorExist = clinic_Database.checkDoctorExist(licenseNo);
            if(doctorExist){
                JOptionPane.showMessageDialog(null,"Doctor with license no. " + licenseNo + " already exist");
                new AddDoctor(currentAdmin).setVisible(true);
                this.dispose();
            }
            else{
                Doctor doc = new Doctor(licenseNo,docPassword,fname,mname,lname,Gender,Long.parseLong(Phone),date,month,year,houseNo,street,
                landmark,city,state,Integer.parseInt(pincode),jdate,jmonth,jyear,"Doctor");
                try{
                    int id = this.currentAdmin.addNewDoctor(doc);
                    JOptionPane.showMessageDialog(null,"New doctor added with id :- " + id);
                    new AdminHome(currentAdmin).setVisible(true);
                    this.dispose();
                }
                catch(SQLException sqle){
                    JOptionPane.showMessageDialog(null,"Cannot add new Doctor");
                    new AdminHome(currentAdmin).setVisible(true);
                    this.dispose();
                    System.out.println("Sql exception occured");
                } 
            }
        }
        catch(SQLException exp){
            System.out.println("SQL error occured " + exp.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

